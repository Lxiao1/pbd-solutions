public class MoreVariables {
    public static void main(String[] args) {
        String name, eyeColour, teethColour, hairColour; //declare strings
        int age, height, weight; //declare integers
        
        name = "Steven";
        age = 15; //a lie
        height = 1;
        weight = 900000;
        eyeColour = "white";
        teethColour = "colourless"; 
        hairColour = "colourless";
        
        System.out.println("Let's talk about " + name + ".");
        System.out.println("He's " + height + " inches tall.");
        System.out.println("He's " + weight + " pounds heavy.");
        System.out.println("Actually, that's not too heavy.");
        System.out.println("He's got " + eyeColour + " eyes and " + hairColour + " hair.");
        System.out.println("His teeth are usually " + teethColour + " depending on the coffee.");
        
        System.out.println("If I add " + age + ", " + height + ", and " + weight
            + " I get " + (age + height + weight) + ".");
    }
}
