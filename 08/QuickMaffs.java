public class QuickMaffs { 
	public static void main(String[] args) {
		System.out.println("I will now do math:"); //comments to the reader "I will now do math"
		System.out.println("1 + 1 = " + (1f + 1f)); //displays 1 + 1 = and then calculates 1+1 and displays it
		System.out.println("2 + 2 - 1 = " + (2f + 2f - 1f)); //displays 2 + 2 - 1 = and then calculates it
		System.out.println( "Is 5 greater than -2? " + ( 5f > -2f ) ); //calculates if 5 > -2
		System.out.println( "Is it greater or equal? " + ( 5f >= -2f ) ); //calculates if 5 >= -2
		System.out.println( "Is it less or equal? " + ( 5f <= -2f ) ); //calculates if 5 <= -2
	}
}
