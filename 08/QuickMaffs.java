		System.out.println("I will now do math:"); //comments to the reader "I will now do math"
		System.out.println("1 + 1 = " + (1.0 + 1.0)); //displays 1 + 1 = and then calculates 1+1 and displays it
		System.out.println("2 + 2 - 1 = " + (1.0 + 1.0 + 1.0)); //displays 2 + 2 - 1 = and then calculates it
